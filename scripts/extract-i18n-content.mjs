/**
 * Extract translatable Sanity content into TS constants for the i18n layer.
 *
 * Run: node scripts/extract-i18n-content.mjs
 *
 * Reads:
 *   - All published `neighborhood` docs → src/lib/copy/neighborhoods.es.ts
 *   - All published `agent` docs        → src/lib/copy/agentes.es.ts
 *
 * Uses the public CDN dataset (no token). If a doc is hidden behind ACL the
 * script will fail loudly and exit non-zero — surface those slugs to Sam so we
 * can decide whether to include them or generate a read token.
 */

import { createClient } from "@sanity/client";
import { readFileSync, writeFileSync, existsSync } from "node:fs";
import { fileURLToPath } from "node:url";
import { dirname, join } from "node:path";

const __dirname = dirname(fileURLToPath(import.meta.url));
const REPO_ROOT = join(__dirname, "..");

// ── Load .env.local manually (no dotenv dep at top level) ────────────────────
function loadEnv() {
  const envPath = join(REPO_ROOT, ".env.local");
  if (!existsSync(envPath)) {
    console.error(`\nMissing ${envPath}\n`);
    process.exit(1);
  }
  const text = readFileSync(envPath, "utf8");
  for (const line of text.split(/\r?\n/)) {
    const m = line.match(/^\s*([A-Z0-9_]+)\s*=\s*"?([^"]*)"?\s*$/);
    if (m && !process.env[m[1]]) process.env[m[1]] = m[2];
  }
}
loadEnv();

const projectId = process.env.NEXT_PUBLIC_SANITY_PROJECT_ID;
const dataset = process.env.NEXT_PUBLIC_SANITY_DATASET ?? "production";

if (!projectId) {
  console.error("\nMissing NEXT_PUBLIC_SANITY_PROJECT_ID in .env.local\n");
  process.exit(1);
}

const client = createClient({
  projectId,
  dataset,
  apiVersion: "2024-01-01",
  useCdn: true,
});

// Serialise a string for inclusion in a TS file via JSON (handles quotes, newlines, etc.).
function tsString(s) {
  return JSON.stringify(s ?? "");
}

async function extractNeighborhoods() {
  console.log("Fetching neighborhoods…");
  const docs = await client.fetch(
    `*[_type == "neighborhood" && defined(slug.current)]{
       "slug": slug.current,
       seoBlock
     } | order(slug asc)`,
  );

  const withSeo = docs.filter((d) => typeof d.seoBlock === "string" && d.seoBlock.trim().length > 0);
  console.log(`  ${docs.length} neighborhoods total, ${withSeo.length} with seoBlock.`);

  const lines = [];
  lines.push("// Spanish neighborhood seoBlocks. Generated by");
  lines.push("// `scripts/extract-i18n-content.mjs`. Re-run when neighborhoods change in Sanity.");
  lines.push("// Manual edits will be overwritten.");
  lines.push("");
  lines.push("export interface NeighborhoodCopy {");
  lines.push("  seoBlock: string;");
  lines.push("}");
  lines.push("");
  lines.push("export const neighborhoodsEs: Record<string, NeighborhoodCopy> = {");
  for (const d of withSeo) {
    lines.push(`  ${tsString(d.slug)}: {`);
    lines.push(`    seoBlock: ${tsString(d.seoBlock)},`);
    lines.push("  },");
  }
  lines.push("};");
  lines.push("");

  const outPath = join(REPO_ROOT, "src", "lib", "copy", "neighborhoods.es.ts");
  writeFileSync(outPath, lines.join("\n"), "utf8");
  console.log(`  Wrote ${outPath}`);
}

async function extractAgents() {
  console.log("Fetching agents…");
  const docs = await client.fetch(
    `*[_type == "agent" && defined(slug.current)]{
       "slug": slug.current,
       name,
       role,
       bio
     } | order(slug asc)`,
  );

  console.log(`  ${docs.length} agents total.`);

  const lines = [];
  lines.push("// Spanish agent bios + roles. Generated by");
  lines.push("// `scripts/extract-i18n-content.mjs`. Re-run when agents change in Sanity.");
  lines.push("// Manual edits will be overwritten.");
  lines.push("//");
  lines.push("// Note: `name` and contact info live in Sanity and are read directly by");
  lines.push("// the agent components. Only `bio` and `role` are translatable.");
  lines.push("");
  lines.push("export interface AgentCopy {");
  lines.push("  bio?: string;");
  lines.push("  role?: string;");
  lines.push("}");
  lines.push("");
  lines.push("export const agentesEs: Record<string, AgentCopy> = {");
  for (const d of docs) {
    const entry = {};
    if (typeof d.bio === "string" && d.bio.trim()) entry.bio = d.bio;
    if (typeof d.role === "string" && d.role.trim()) entry.role = d.role;
    if (Object.keys(entry).length === 0) continue;
    lines.push(`  ${tsString(d.slug)}: {`);
    if (entry.bio) lines.push(`    bio: ${tsString(entry.bio)},`);
    if (entry.role) lines.push(`    role: ${tsString(entry.role)},`);
    lines.push("  },");
  }
  lines.push("};");
  lines.push("");

  const outPath = join(REPO_ROOT, "src", "lib", "copy", "agentes.es.ts");
  writeFileSync(outPath, lines.join("\n"), "utf8");
  console.log(`  Wrote ${outPath}`);
}

(async () => {
  try {
    await extractNeighborhoods();
    await extractAgents();
    console.log("\nDone.");
  } catch (err) {
    console.error("\nExtraction failed:");
    console.error(err);
    process.exit(1);
  }
})();
